def get_all_skills():
    all_skills = set()
    for category_skills in SKILLS_DATABASE.values():
        all_skills.update(category_skills)
    return all_skills


def normalize_skill(skill):
    skill_lower = skill.lower().strip()
    return SKILL_ALIASES.get(skill_lower, skill_lower)


def get_skill_category(skill):
    skill_normalized = normalize_skill(skill)
    for category, skills in SKILLS_DATABASE.items():
        if skill_normalized in skills:
            return category
    return None


def get_category_count():
    return len(SKILLS_DATABASE)


def get_total_skills():
    return len(get_all_skills())


def get_category_stats():
    return {
        category: len(skills) 
        for category, skills in SKILLS_DATABASE.items()
    }
